//体力は敵に射撃されると減る。
//
//・ウェーブ式のゲームにしてタワーディフェンス要素を持たせる
//
//[10]ウェーブ事に、ショップを開放
//スキルを買える。
// 
//[１]ウェーブにつき合計[100]体の敵を出現
//
//全部死なずに倒したら次ウェーブに
//
//ウェーブが始まると電気が落ち、換気扇が止まる。
//段々減るようにして10秒後に最大に減るようにする。
//
//ウェーブの合間で電気が通り、換気扇が回ってゲージが回復する。
//これも段々回復するようにする。１０秒でマックス回復。
// 
//ウェーブの合間は[15]秒。スキップボタンを追加する。
//
//・各ゲージ処理（予定）
//
//全てをまとめたゲージの名前
//【血液酸素ゲージ:fBloodOxygen】　値：[1000]
//
//時間で減る量（酸素供給量）　値：毎秒 最大[200]~最小[0](緩やかに上昇させたい)
//弾薬で減る量（弾薬消費量）　値：銃によって変える予定だが 初期ハンドガンは一発[20]
